// ────────────────────────────────────────────────────────────────────
// Spectrum / waveform — animates while a track is playing in the store
// ────────────────────────────────────────────────────────────────────
function Spectrum({ live, bars = 42, className = '' }: { live: boolean; bars?: number; className?: string }) {
  const [t, setT] = useState(0);
  useEffect(() => {
    if (!live) return;
    let raf = 0;
    const loop = () => {
      setT((p) => p + 1);
      raf = requestAnimationFrame(loop);
    };
    raf = requestAnimationFrame(loop);
    return () => cancelAnimationFrame(raf);
  }, [live]);
  return (
    <div className={`flex items-end gap-[2px] h-9 w-full ${className}`}>
      {Array.from({ length: bars }).map((_, i) => {
        const v = live
          ? Math.abs(Math.sin(t / 14 + i * 0.45) * 0.55 + Math.sin(t / 9 + i * 0.12) * 0.4) * 100
          : 30 + ((i * 13) % 50);
        const color = i % 5 === 0 ? 'var(--brand-3, #2dd4bf)' : i % 3 === 0 ? 'var(--brand)' : 'var(--brand-2)';
        return (
          <span
            key={i}
            className="flex-1 min-w-[2px] rounded-t-[2px] opacity-85 transition-[height] duration-[120ms] ease-out"
            style={{ height: `${10 + v * 0.8}%`, background: color }}
          />
        );
      })}
    </div>
  );
}

function SpectrumLarge({ live }: { live: boolean }) {
  const [t, setT] = useState(0);
  useEffect(() => {
    let raf = 0;
    const loop = () => {
      setT((p) => p + 1);
      raf = requestAnimationFrame(loop);
    };
    raf = requestAnimationFrame(loop);
    return () => cancelAnimationFrame(raf);
  }, []);
  const bars = 80;
  return (
    <div className="flex items-end gap-[3px] h-[220px] w-full mt-12">
      {Array.from({ length: bars }).map((_, i) => {
        const dist = Math.abs(i - bars / 2) / (bars / 2);
        const env = 1 - dist * dist * 0.55;
        const v = live
          ? Math.abs(Math.sin(t / 12 + i * 0.32) * 0.5 + Math.sin(t / 7 + i * 0.09) * 0.5) * env
          : Math.abs(Math.sin(i * 0.32) * 0.5 + Math.sin(i * 0.09) * 0.5) * env;
        const h = 14 + v * 86;
        const color = i % 9 === 0 ? 'var(--brand-3, #2dd4bf)' : i % 4 === 0 ? 'var(--brand)' : 'var(--brand-2)';
        return (
          <span
            key={i}
            className="flex-1 min-w-[2px] rounded-t-[4px] opacity-90 transition-[height] duration-[120ms] ease-out"
            style={{ height: `${h}%`, background: color, boxShadow: `0 0 20px ${color}` }}
          />
        );
      })}
    </div>
  );
}

// ────────────────────────────────────────────────────────────────────
// Procedural cover art (when a track has no coverArt)
// ────────────────────────────────────────────────────────────────────
function CoverArt({ seed = '', src }: { seed?: string; src?: string | null }) {
  const hue = useMemo(() => {
    let h = 0;
    for (let i = 0; i < seed.length; i++) h = (h * 31 + seed.charCodeAt(i)) % 360;
    return h || 290;
  }, [seed]);

  if (src) {
    return <img src={src} alt="" className="absolute inset-0 w-full h-full object-cover" />;
  }
  return (
    <div
      className="absolute inset-0"
      style={{
        background: `radial-gradient(120% 90% at 25% 20%, hsl(${hue} 90% 65%) 0%, hsl(${(hue + 60) % 360} 70% 35%) 45%, #0a0a0d 100%)`,
      }}
    >
      <div className="absolute right-[12%] top-[12%]">
        <span className="absolute right-0 top-0 w-10 h-10 rounded-full border-[1.5px] border-white/35" />
        <span className="absolute -right-2.5 -top-2.5 w-[60px] h-[60px] rounded-full border-[1.5px] border-white/35 opacity-60" />
        <span className="absolute -right-[22px] -top-[22px] w-[84px] h-[84px] rounded-full border-[1.5px] border-white/35 opacity-30" />
      </div>
    </div>
  );
}

