// ────────────────────────────────────────────────────────────────────
// Hero — interactable Deck player. Click play => actually plays a track.
// ────────────────────────────────────────────────────────────────────
function HeroDeck({ tracks }: { tracks: TrackItem[] }) {
  const { currentTrack, isPlaying, playTrack, togglePlay, nextTrack, prevTrack, progress, duration } = usePlayerStore();
  const [autoIdx, setAutoIdx] = useState(0);

  // When nothing is playing, auto-cycle the highlighted track in the deck
  useEffect(() => {
    if (currentTrack || tracks.length === 0) return;
    const i = setInterval(() => setAutoIdx((x) => (x + 1) % tracks.length), 3500);
    return () => clearInterval(i);
  }, [currentTrack, tracks.length]);

  const visibleQueue = tracks.slice(0, 4);
  const featured = currentTrack ?? tracks[autoIdx];
  const fmt = (s: number) => {
    if (!s || !Number.isFinite(s)) return '0:00';
    const m = Math.floor(s / 60);
    const r = Math.floor(s % 60);
    return `${m}:${r.toString().padStart(2, '0')}`;
  };
  const progressPct = duration > 0 ? Math.min(100, (progress / duration) * 100) : 32;

  const handlePlay = () => {
    if (!featured) return;
    if (currentTrack && currentTrack.id === featured.id) {
      togglePlay();
    } else {
      playTrack(featured, tracks);
    }
  };

  return (
    <div
      className="relative rounded-[22px] p-4 sm:p-5 overflow-hidden"
      style={{
        background: 'linear-gradient(180deg, var(--bg-elev-2), var(--bg-elev-1))',
        border: '1px solid var(--stroke-strong)',
        boxShadow: '0 30px 60px -30px rgba(0,0,0,0.6)',
      }}
    >
      <div className="flex items-center justify-between mb-3.5">
        <div
          className="text-[10px] font-bold uppercase px-2.5 py-1 rounded"
          style={{ letterSpacing: '0.25em', color: 'var(--brand)', background: 'var(--brand-soft)' }}
        >
          NOW PLAYING · STATION 88.1
        </div>
        <div className="flex gap-1">
          <span className="w-2 h-2 rounded-full" style={{ background: '#2dd4bf', boxShadow: '0 0 8px #2dd4bf' }} />
          <span className="w-2 h-2 rounded-full" style={{ background: '#f59e0b', boxShadow: '0 0 8px #f59e0b' }} />
          <span className="w-2 h-2 rounded-full bg-white/15" />
        </div>
      </div>

      <div className="grid grid-cols-[100px_1fr] sm:grid-cols-[130px_1fr] gap-4">
        <div className="relative w-[100px] h-[100px] sm:w-[130px] sm:h-[130px] rounded-[14px] overflow-hidden">
          <CoverArt seed={featured?.title || 'demo'} src={featured?.coverArt ?? undefined} />
        </div>
        <div className="min-w-0 flex flex-col gap-1">
          <div className="font-display font-extrabold text-base sm:text-lg leading-tight truncate">
            {featured?.title ?? 'Rain on Neon Glass'}
          </div>
          <div className="text-[13px] truncate" style={{ color: 'var(--text-soft)' }}>
            {featured?.agent?.name ?? 'Midnight Lo-Fi Engine'}
          </div>
          <div className="text-[11.5px] mb-2.5 truncate" style={{ color: 'var(--text-mute)' }}>
            {featured?.genre?.name ?? 'cinematic synthwave'}
          </div>
          <Spectrum live={isPlaying && !!currentTrack} bars={36} />
          <div className="flex items-center gap-2 mt-auto pt-2">
            <button
              onClick={prevTrack}
              disabled={!currentTrack}
              aria-label="Previous track"
              className="p-1.5 transition-colors disabled:opacity-30"
              style={{ color: 'var(--text-soft)' }}
            >
              <SkipBack className="w-4 h-4" fill="currentColor" />
            </button>
            <button
              onClick={handlePlay}
              aria-label={isPlaying && currentTrack?.id === featured?.id ? 'Pause' : 'Play'}
              className="w-[42px] h-[42px] rounded-full inline-flex items-center justify-center text-white border-0 cursor-pointer transition-all"
              style={{
                background: 'var(--brand)',
                boxShadow: '0 12px 26px -8px var(--brand-glow)',
              }}
              onMouseEnter={(e) => {
                e.currentTarget.style.transform = 'scale(1.05)';
                e.currentTarget.style.background = 'var(--brand-strong)';
              }}
              onMouseLeave={(e) => {
                e.currentTarget.style.transform = 'scale(1)';
                e.currentTarget.style.background = 'var(--brand)';
              }}
            >
              {isPlaying && currentTrack?.id === featured?.id ? (
                <Pause className="w-4 h-4" fill="currentColor" />
              ) : (
                <PlayGlyph size={14} />
              )}
            </button>
            <button
              onClick={nextTrack}
              disabled={!currentTrack}
              aria-label="Next track"
              className="p-1.5 transition-colors disabled:opacity-30"
              style={{ color: 'var(--text-soft)' }}
            >
              <SkipForward className="w-4 h-4" fill="currentColor" />
            </button>
            <div className="flex-1 min-w-0 ml-1">
              <div className="h-1 rounded-full overflow-hidden" style={{ background: 'rgba(255,255,255,0.08)' }}>
                <div
                  className="h-full rounded-full transition-[width] duration-100"
                  style={{ width: `${progressPct}%`, background: 'var(--aurora)' }}
                />
              </div>
              <div
                className="flex justify-between text-[10.5px] mt-1 tabular-nums"
                style={{ color: 'var(--text-mute)' }}
              >
                <span>{currentTrack ? fmt(progress) : '1:24'}</span>
                <span>{featured?.duration ? fmt(featured.duration) : '3:48'}</span>
              </div>
            </div>
          </div>
        </div>
      </div>

      <div className="mt-4 pt-3 border-t flex flex-col gap-0.5" style={{ borderColor: 'var(--stroke)' }}>
        {visibleQueue.map((track, i) => {
          const isNow = currentTrack ? currentTrack.id === track.id : i === autoIdx % visibleQueue.length;
          return (
            <button
              key={track.id}
              onClick={() => playTrack(track, tracks)}
              className="grid grid-cols-[24px_1fr_auto] items-center gap-2.5 px-2 py-1.5 rounded-lg text-[12.5px] text-left transition-colors hover:bg-white/[0.04]"
              style={isNow ? { background: 'var(--brand-soft)' } : undefined}
            >
              <span
                className="text-[11px] tabular-nums"
                style={{ color: isNow ? 'var(--brand)' : 'var(--text-faint)' }}
              >
                {String(i + 1).padStart(2, '0')}
              </span>
              <span className="truncate">{track.title}</span>
              <span className="text-[11px] tabular-nums" style={{ color: 'var(--text-mute)' }}>
                {fmt(track.duration)}
              </span>
            </button>
          );
        })}
        {visibleQueue.length === 0 && (
          <div className="px-2 py-3 text-xs" style={{ color: 'var(--text-mute)' }}>
            Loading queue…
          </div>
        )}
      </div>
    </div>
  );
}

