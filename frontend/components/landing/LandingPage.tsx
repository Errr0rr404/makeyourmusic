// ────────────────────────────────────────────────────────────────────
// Top-level landing page
// ────────────────────────────────────────────────────────────────────
export function LandingPage() {
  const [tracks, setTracks] = useState<TrackItem[]>([]);
  const [genres, setGenres] = useState<Array<{ name: string; count: string; hue: number; slug: string }>>(FALLBACK_GENRES);

  useEffect(() => {
    track('landing_view');
  }, []);

  useEffect(() => {
    let cancelled = false;
    api
      .get('/tracks/trending')
      .then((res) => {
        if (cancelled) return;
        const fetched = (res.data?.tracks || []) as TrackItem[];
        if (fetched.length > 0) setTracks(fetched);
      })
      .catch(() => undefined);

    api
      .get('/genres')
      .then((res) => {
        if (cancelled) return;
        type ApiGenre = { name?: string; slug?: string; color?: string | null; _count?: { tracks?: number } };
        const fetched: ApiGenre[] = res.data?.genres || [];
        if (fetched.length === 0) return;
        const mapped = fetched.slice(0, 8).map((g, i) => {
          const hueFromColor = colorToHue(g.color ?? null);
          const fallbackHue = FALLBACK_GENRES[i % FALLBACK_GENRES.length]?.hue ?? 290;
          return {
            name: g.name ?? 'Unknown',
            slug: g.slug ?? (g.name ?? 'unknown').toLowerCase().replace(/\s+/g, '-'),
            count: (g._count?.tracks ?? 0).toLocaleString(),
            hue: hueFromColor ?? fallbackHue,
          };
        });
        setGenres(mapped);
      })
      .catch(() => undefined);

    return () => {
      cancelled = true;
    };
  }, []);

  return (
    <div className="mym-landing-root">
      <HeroDeckLayout tracks={tracks} />

      <MarqueeStrip />
      <DiscoverGrid tracks={tracks} />
      <HowItWorks />
      <Genres genres={genres} />
      <MobileSection tracks={tracks} />
      <Pricing />
      <FAQ />
      <CTABand tracks={tracks} />
      <StickyMobileCTA />
    </div>
  );
}

